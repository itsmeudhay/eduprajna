  export const transition = () => {
    return {
      duration: 2.50,
      delay: 0.2,
      ease: "easeIn",
    };
  };
  
  export const fadeIn = (direction) => {
    return {
      hidden: {
        opacity: 0,
        y: direction === "down" ? -85 : 85,
      },
      visible: { opacity: 1, y: 0 },
    };
  };
  
  
  export const scale = () => {
    return {
      hidden: {
        scale: 0,
        opacity: 0,
      },
      visible: { scale: 1, opacity: 1 },
    };
  };
  
  export const reveal = () => {
    return {
      hidden: {
        left: 0,
      },
      visible: { left: "100%" },
    };
  };
  